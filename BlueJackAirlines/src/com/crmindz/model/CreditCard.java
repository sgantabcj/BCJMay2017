package com.crmindz.model;

public class CreditCard {
	private String nameOnCard;
	private String cardNumber;
	private String cardExp;
	private String saveChkBx;
	private int cvv;
	
	private Double cardBalance;

	/**
	 * @return the nameOnCard
	 */
	public String getNameOnCard() {
		return nameOnCard;
	}

	/**
	 * @param nameOnCard the nameOnCard to set
	 */
	public void setNameOnCard(String nameOnCard) {
		this.nameOnCard = nameOnCard;
	}

	/**
	 * @return the cardNumber
	 */
	public String getCardNumber() {
		return cardNumber;
	}

	/**
	 * @param cardNumber the cardNumber to set
	 */
	public void setCardNumber(String cardNumber) {
		this.cardNumber = cardNumber;
	}

	/**
	 * @return the cardExp
	 */
	public String getCardExp() {
		return cardExp;
	}

	/**
	 * @param cardExp the cardExp to set
	 */
	public void setCardExp(String cardExp) {
		this.cardExp = cardExp;
	}

	/**
	 * @return the saveChkBx
	 */
	public String getSaveChkBx() {
		return saveChkBx;
	}

	/**
	 * @param saveChkBx the saveChkBx to set
	 */
	public void setSaveChkBx(String saveChkBx) {
		this.saveChkBx = saveChkBx;
	}

	/**
	 * @return the cvv
	 */
	public int getCvv() {
		return cvv;
	}

	/**
	 * @param cvv the cvv to set
	 */
	public void setCvv(int cvv) {
		this.cvv = cvv;
	}

	/**
	 * @return the cardBalance
	 */
	public Double getCardBalance() {
		return cardBalance;
	}

	/**
	 * @param cardBalance the cardBalance to set
	 */
	public void setCardBalance(Double cardBalance) {
		this.cardBalance = cardBalance;
	}
	
	
}
