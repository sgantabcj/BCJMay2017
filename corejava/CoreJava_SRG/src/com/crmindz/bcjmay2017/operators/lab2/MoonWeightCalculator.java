package com.crmindz.bcjmay2017.operators.lab2;

public class MoonWeightCalculator {
	public static void main(String[] args) {
		double weightOnEarth = 100;
		double weightOnMoon = getMoonWeight(weightOnEarth);
		System.out.println("If weight on earth is: "+weightOnEarth+" weight on moon will be: "+weightOnMoon);
		
	}
	public static double getMoonWeight(double weight){
		return weight*0.17;
	}
}
