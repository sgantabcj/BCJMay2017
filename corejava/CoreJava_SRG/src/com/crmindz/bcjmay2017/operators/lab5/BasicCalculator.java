package com.crmindz.bcjmay2017.operators.lab5;

import java.util.Scanner;

public class BasicCalculator {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.print("enter the values to perform arithmetic operations:");
		int valueOne = sc.nextInt();
		int valueTwo = sc.nextInt();
		sc.close();
		
		System.out.println(valueOne+"+"+valueTwo+" = "+(valueOne+valueTwo)
							+"\n"+valueOne+"-"+valueTwo+" = "+(valueOne-valueTwo)
							+"\n"+valueOne+"*"+valueTwo+" = "+(valueOne*valueTwo)
							+"\n"+valueOne+"/"+valueTwo+" = "+valueOne/valueTwo);
	}
}
