package com.crmindz.bcjmay2017.flowcontrol.lab3;

public class SwitchCase {
	public static void main(String[] args) {
		System.out.printf("For grade: %c range of marks is: %s",'A',getRangeOfMarks('z'));
	}
	public static String getRangeOfMarks(char grade){
		switch(Character.toUpperCase(grade)){
			case 'A':
				return "80-100";
			case 'B':
				return "73-79";
			case 'C':
				return "65-72";
			case 'D':
				return "55-64";
			case 'E':
				return "<55";
			default:
				return "Grade does not exist";
		}
	}
}
