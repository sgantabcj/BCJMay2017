package com.crmindz.bcjmay2017.oop.overloading.lab1;

public class PrintDetails {
	public void printHeader(char c){
		for(int i=0;i<80;i++){
			System.out.print(c);
		}
	}
	public void printHeader(char c, int n){
		for(int i=0;i<n;i++){
			System.out.print(c);
		}
	}
	public void printHeader(String s){
			System.out.println(s);
	}
	
}
