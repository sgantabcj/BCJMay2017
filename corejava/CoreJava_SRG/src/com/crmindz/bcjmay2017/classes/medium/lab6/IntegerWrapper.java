package com.crmindz.bcjmay2017.classes.medium.lab6;

public class IntegerWrapper {
	 private int i;
	 public IntegerWrapper(int a) { 
		 this.i = a; 
		}
	 public void swap(IntegerWrapper intWrapper1, IntegerWrapper intWrapper2){
		int temp = intWrapper1.getI();
		intWrapper1.setI(intWrapper2.getI());
		intWrapper2.setI(temp);
	 }
	/**
	 * @return the i
	 */
	public int getI() {
		return i;
	}
	/**
	 * @param i the i to set
	 */
	public void setI(int i) {
		this.i = i;
	}
}
